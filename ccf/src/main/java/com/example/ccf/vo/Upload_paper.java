package com.example.ccf.vo;

public class Upload_paper {
    private String title;
    private String abstracts;
    private String doi;
    private int citation;
    private String book_title;
    private String pages;
    private String publisher;
    private String year;
    private String url;
    private String time;
    private String bib_url;
    private String bib_source;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAbstracts() {
        return abstracts;
    }

    public void setAbstracts(String abstracts) {
        this.abstracts = abstracts;
    }

    public String getDoi() {
        return doi;
    }

    public void setDoi(String doi) {
        this.doi = doi;
    }

    public int getCitation() {
        return citation;
    }

    public void setCitation(int citation) {
        this.citation = citation;
    }

    public String getBook_title() {
        return book_title;
    }

    public void setBook_title(String book_title) {
        this.book_title = book_title;
    }

    public String getPages() {
        return pages;
    }

    public void setPages(String pages) {
        this.pages = pages;
    }

    public String getPublisher() {
        return publisher;
    }

    public void setPublisher(String publisher) {
        this.publisher = publisher;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getBib_url() {
        return bib_url;
    }

    public void setBib_url(String bib_url) {
        this.bib_url = bib_url;
    }

    public String getBib_source() {
        return bib_source;
    }

    public void setBib_source(String bib_source) {
        this.bib_source = bib_source;
    }
}
